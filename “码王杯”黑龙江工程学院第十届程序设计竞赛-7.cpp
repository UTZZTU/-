void solve()
{
	a[1]=a[2]=a[3]=0;
	js=0;
	minn=0x3f3f3f3f;
	cin>>s;
	n=s.sz();
	for(int l=0,r=-1;l<n;l++)
	{
		if(js==3)
		{
			minn=min(minn,r-l+1);
		}
		else
		{
			while(r+1<n)
			{
				r++;
				a[s[r]-'0']++;
				if(a[s[r]-'0']==1) js++;
				if(js==3) break;
			}
			if(js==3) minn=min(minn,r-l+1);
		}
		a[s[l]-'0']--;
		if(a[s[l]-'0']==0) js--;
	}
	if(minn==0x3f3f3f3f) pr(0);
	else pr(minn);
}
