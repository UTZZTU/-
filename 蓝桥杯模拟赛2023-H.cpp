#include <bits/stdc++.h>
using namespace std;
int k,n=8,m=8,minn=0x3f3f3f3f,pos;
int num(int x)
{
	int res=0;
	while(x)
	{
		res+=x%10;
		x/=10;
	}
	return res;
}
int main ()
{
	for(int i=1;i<=n;i++)
	{
		for(int j=1;j<=m;j++)
		{
			scanf("%d",&k);
			if(num(k)<minn)
			{
				minn=num(k);
				pos=k;
			}
		}
	}
	printf("%d",pos);
	return 0;
}
